import UIKit

extension UIFont {
    static func semiBold20() -> UIFont? {
        return UIFont.init(name: "SFUIDisplay-Regular", size: 20)
    }

    static func semiBold26() -> UIFont? {
        return UIFont.init(name: "SFUIDisplay-Semibold", size: 26)
    }
}
