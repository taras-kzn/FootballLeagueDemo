import UIKit

extension UIColor {
    static func textFieldBottomViewColor() -> UIColor {
        return UIColor.init(red: 0.937, green: 0.937, blue: 0.945, alpha: 1)

    }

    static func backgroundLightColor() -> UIColor {
        return UIColor.init(red: 0.784, green: 0.781, blue: 0.805, alpha: 1)
    }

    static func backgroundHeavyColor() -> UIColor {
        return UIColor.init(red: 0.655, green: 0.655, blue: 0.658, alpha: 1)
    }
}
